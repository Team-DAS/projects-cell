package com.udeajobs.projects_cell.categorization_service.enums;

/**
 * Enum que representa los diferentes tipos de categorías en el sistema.
 *
 * @author UdeaJobs Team
 */
public enum CategoryType {
    /**
     * Habilidad técnica (ej: Java, Python, React)
     */
    SKILL,

    /**
     * Etiqueta general (ej: Remote, Urgent, Featured)
     */
    TAG,

    /**
     * Nivel de experiencia laboral (ej: Junior, Senior, Expert)
     */
    JOB_LEVEL,

    /**
     * Industria o sector (ej: Technology, Healthcare, Finance)
     */
    INDUSTRY
}

